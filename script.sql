-- Create the employee_surveys table
CREATE TABLE IF NOT EXISTS employee_survey (
    employee_id VARCHAR(10) PRIMARY KEY,
    hiring_duration_weeks DECIMAL(4,1),
    hiring_satisfaction INT CHECK (hiring_satisfaction BETWEEN 1 AND 10),
    onboarding_rating INT CHECK (onboarding_rating BETWEEN 1 AND 5),
    recommend_company INT CHECK (recommend_company BETWEEN 1 AND 10),
    job_satisfaction INT CHECK (job_satisfaction BETWEEN 1 AND 10),
    reason_for_leaving VARCHAR(100),
    stayed BOOLEAN
);

